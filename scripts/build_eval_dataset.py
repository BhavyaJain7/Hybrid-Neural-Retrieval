#!/usr/bin/env python3
"""
scripts/build_eval_dataset.py

Fixes retrieval bias in the eval dataset by:
1. Loading queries generated by generate_queries.py
2. For each query, retrieving candidates WITHOUT showing top results first
3. Injecting the ground-truth chunk (from relevance.json) at a random position
4. Running multi-retriever fusion to surface additional candidates
5. Presenting candidates in RANDOM order — not ranked order — to the labeler
   This breaks the bias of labeling whatever the retriever ranked #1.

Usage:
    python scripts/build_eval_dataset.py \
        --collection <slug> \
        --output evaluation \
        --k 15 \
        --mode hybrid
"""
from __future__ import annotations

import argparse
import json
import random
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent / "src"))

from neural_search.retrieval.dense import QdrantRetriever
from neural_search.retrieval.hybrid import HybridRetriever
from neural_search.retrieval.sparse import BM25sRetriever


class _Quit(Exception):
    pass


def load_json(path: Path, default):
    if path.exists():
        return json.loads(path.read_text())
    return default


def save_json(data, path: Path):
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2))


def get_retriever(collection: str, mode: str):
    sparse = BM25sRetriever(collection_slug=collection)
    sparse.load()
    dense = QdrantRetriever(collection_slug=collection)
    if mode == "sparse":
        return sparse
    if mode == "dense":
        return dense
    return HybridRetriever(sparse=sparse, dense=dense)


def _load_snapshot_index(collection: str) -> dict[str, dict]:
    """Build chunk_id → chunk lookup from snapshot for ground-truth injection."""
    from neural_search.config import get_settings
    settings = get_settings()
    snapshot = settings.snapshot_path_for(collection)
    index = {}
    if snapshot.exists():
        with open(snapshot) as f:
            for line in f:
                line = line.strip()
                if line:
                    chunk = json.loads(line)
                    index[chunk["chunk_id"]] = chunk
    return index


def _print_result(position: int, r: dict, is_ground_truth: bool = False):
    gt_marker = " ← [GT]" if is_ground_truth else ""
    print(f"\n[{position}]{gt_marker}")
    print(f"  chunk_id : {r.get('chunk_id')}")
    src = r.get("source_file", "unknown")
    page = r.get("page", "-")
    print(f"  source   : {src} (page {page})")
    text = r.get("text", "")
    print(f"  text     : {text[:250]}...")


def _label_query(
    query: dict,
    candidates: list[dict],
    existing_relevance: dict,
    override: bool,
    ground_truth_ids: list[str],
) -> list[str] | None:
    """
    Present candidates in RANDOM order to avoid retrieval rank bias.
    Ground-truth chunks are injected into the candidate pool if not already present.
    Returns list of relevant chunk_ids, or None if skipped.
    """
    qid = query["id"]

    if qid in existing_relevance and not override:
        print(f"[SKIP] {qid} already labeled — use --override to redo")
        return existing_relevance[qid]

    print("\n" + "=" * 80)
    print(f"Query ID : {qid}")
    print(f"Type     : {query.get('type', '?')}")
    print(f"Query    : {query['text']}")
    print("=" * 80)
    print("NOTE: Results shown in RANDOM order (not ranked) to avoid labeling bias.")
    print("      Mark ALL chunks that answer the query, not just the best one.\n")

    # Shuffle candidates — this is the core bias fix
    display = list(candidates)
    random.shuffle(display)

    relevant_ids: list[str] = []
    i = 0

    while i < len(display):
        r = display[i]
        is_gt = r["chunk_id"] in ground_truth_ids
        _print_result(i + 1, r, is_ground_truth=is_gt)
        cmd = input(
            "  ENTER/1=relevant  0=not relevant  s=skip query  r=restart  q=quit >> "
        ).strip().lower()

        if cmd == "q":
            raise _Quit
        elif cmd == "s":
            print(f"  [SKIP] {qid}")
            return None
        elif cmd == "r":
            print("  Restarting query...\n")
            return _label_query(query, candidates, existing_relevance, True, ground_truth_ids)
        elif cmd in ("", "1"):
            relevant_ids.append(r["chunk_id"])
            i += 1
        elif cmd == "0":
            i += 1
        else:
            print("  Invalid input — try again")

    print(f"\n  → {len(relevant_ids)} relevant chunks marked")

    # Warn if labeler missed the ground-truth chunk
    missed_gt = [cid for cid in ground_truth_ids if cid not in relevant_ids]
    if missed_gt:
        print(f"  [WARN] Ground-truth chunk(s) not marked relevant: {missed_gt}")
        confirm = input("  Add them anyway? (y/n) >> ").strip().lower()
        if confirm == "y":
            for cid in missed_gt:
                if cid not in relevant_ids:
                    relevant_ids.append(cid)

    return relevant_ids


def _build_candidate_pool(
    query_text: str,
    retriever,
    snapshot_index: dict[str, dict],
    ground_truth_ids: list[str],
    k: int,
) -> list[dict]:
    """
    Retrieve k candidates, then inject ground-truth chunk if missing.
    This ensures labeler always sees the correct answer, regardless of retrieval rank.
    """
    results = retriever.search(query_text, k=k)

    # Normalize result format
    candidates = []
    seen_ids = set()
    for r in results:
        cid = r.get("chunk_id", "")
        if cid and cid not in seen_ids:
            candidates.append(r)
            seen_ids.add(cid)

    # Inject ground-truth chunks if not retrieved
    for gt_id in ground_truth_ids:
        if gt_id not in seen_ids and gt_id in snapshot_index:
            gt_chunk = snapshot_index[gt_id]
            candidates.append({
                "chunk_id": gt_chunk.get("chunk_id", gt_id),
                "text": gt_chunk.get("text", ""),
                "source_file": gt_chunk.get("source_file", ""),
                "page": gt_chunk.get("page", 0),
                "score": 0.0,
                "rrf_score": 0.0,
                "_injected": True,  # debug marker
            })
            seen_ids.add(gt_id)

    return candidates


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--collection", required=True)
    parser.add_argument("--output", default="evaluation")
    parser.add_argument("--k", type=int, default=15,
                        help="Candidates per query (higher = harder labeling task)")
    parser.add_argument("--mode", choices=["hybrid", "dense", "sparse"], default="hybrid")
    parser.add_argument("--override", action="store_true",
                        help="Re-label already labeled queries")
    parser.add_argument("--type-filter", choices=["keyword", "semantic", "vague"],
                        help="Only label queries of this type")
    parser.add_argument("--seed", type=int, default=42)
    args = parser.parse_args()

    random.seed(args.seed)
    out = Path(args.output)
    queries_path = out / "queries.json"
    relevance_path = out / "relevance.json"

    queries: list[dict] = load_json(queries_path, [])
    relevance: dict = load_json(relevance_path, {})

    if not queries:
        print("[ERROR] No queries found.")
        print("Run scripts/generate_queries.py first.")
        sys.exit(1)

    # Filter by type if requested
    if args.type_filter:
        queries = [q for q in queries if q.get("type") == args.type_filter]
        print(f"[INFO] Filtered to {len(queries)} '{args.type_filter}' queries")

    retriever = get_retriever(args.collection, args.mode)
    snapshot_index = _load_snapshot_index(args.collection)

    unlabeled = [q for q in queries if q["id"] not in relevance or args.override]
    print(f"\n[INFO] Mode         : {args.mode}")
    print(f"[INFO] Candidates/query : {args.k}")
    print(f"[INFO] Total queries    : {len(queries)}")
    print(f"[INFO] Already labeled  : {len(relevance)}")
    print(f"[INFO] To label         : {len(unlabeled)}")
    print(f"\n[BIAS FIX] Candidates shown in random order — not retrieval rank order.")
    print(f"[BIAS FIX] Ground-truth chunks injected into pool if not retrieved.\n")

    labeled_this_run = 0

    try:
        for q in unlabeled:
            ground_truth_ids = relevance.get(q["id"], [])

            candidates = _build_candidate_pool(
                query_text=q["text"],
                retriever=retriever,
                snapshot_index=snapshot_index,
                ground_truth_ids=ground_truth_ids,
                k=args.k,
            )

            labels = _label_query(
                query=q,
                candidates=candidates,
                existing_relevance=relevance,
                override=args.override,
                ground_truth_ids=ground_truth_ids,
            )

            if labels is not None:
                relevance[q["id"]] = labels
                labeled_this_run += 1
                save_json(relevance, relevance_path)
                print(f"  [SAVED] {relevance_path} ({len(relevance)} total labeled)\n")

    except _Quit:
        print("\n[EXIT] Quitting early — progress saved.")
    except KeyboardInterrupt:
        print("\n[EXIT] Interrupted — progress saved.")

    print(f"\n{'='*50}")
    print(f"Labeled this run : {labeled_this_run}")
    print(f"Total labeled    : {len(relevance)} / {len(queries)}")

    # Distribution report
    type_labeled = {}
    for q in queries:
        if q["id"] in relevance:
            t = q.get("type", "unknown")
            type_labeled[t] = type_labeled.get(t, 0) + 1
    print(f"By type          : {type_labeled}")
    print(f"Relevance file   : {relevance_path}")
    print(f"{'='*50}")

    if len(relevance) >= 50:
        print("\n✓ Dataset ready — run scripts/run_eval.py to benchmark")
    else:
        remaining = 50 - len(relevance)
        print(f"\n✗ Need {remaining} more labeled queries before running eval")



if __name__ == "__main__":
    main()
